package com.pi9Lin.data;

import android.graphics.Bitmap;

public class NearBy {
	String cover;
	int count;
	Bitmap bp;
	public String getCover() {
		return cover;
	}
	public void setCover(String cover) {
		this.cover = cover;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public Bitmap getBp() {
		return bp;
	}
	public void setBp(Bitmap bp) {
		this.bp = bp;
	}
	
}
